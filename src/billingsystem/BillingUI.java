    private void registerKeyBindings() {
        // Get the root pane of the JFrame
        JRootPane rootPane = this.getRootPane();

        // Create a KeyStroke for the F4 key
        KeyStroke keyStroke = KeyStroke.getKeyStroke(KeyEvent.VK_F4, 0);

        // Create an Action for the key binding
        Action action = new AbstractAction() {
            @Override
            public void actionPerformed(ActionEvent e) {
                DefaultTableModel model = (DefaultTableModel) tblItems.getModel();

                // Remove the last row from the model
                if (model.getRowCount() > 0) {
                    model.removeRow(model.getRowCount() - 1);
                }
            }
        };

        // Add the key binding to the InputMap and ActionMap of the root pane
        rootPane.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(keyStroke, "F4");
        rootPane.getActionMap().put("F4", action);
}

