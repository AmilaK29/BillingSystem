    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        ArrayList<Object> arrItemInfo = dbo.getItem(txtBarCode.getText());
        float quantity = Float.parseFloat(txtAmount.getText());
        arrItemInfo.add(2,quantity);
        double total = (double) arrItemInfo.get(3) * quantity;
        DecimalFormat df = new DecimalFormat("#.##");
        double rounded_total = Double.parseDouble(df.format(total));
        totalPrice = totalPrice + rounded_total;
        txtTotal.setText(Double.toString(totalPrice));
        arrItemInfo.add(0);
        arrItemInfo.add(rounded_total);
        
        ArrayList<Object> itms = new ArrayList<Object>();
        itms.add(arrItemInfo.get(1));
        itms.add(quantity);
        itms.add(total);
        
        Object [] itmsArray = itms.toArray();
        
        items.add(itmsArray);
        
        
        
        model.addRow(arrItemInfo.toArray());
        tblItems.setModel(model);
        
    }//GEN-LAST:event_btnAddActionPerformed
